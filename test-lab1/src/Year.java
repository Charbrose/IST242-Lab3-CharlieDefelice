// A class to represent the year of a motorcycle
class Year {
    // A private field to store the year
    private int year;

    // A constructor to initialize the year
    public Year(int year) {
        this.year = year;
    }

    // A getter method to return the year
    public int getYear() {
        return year;
    }

    // A setter method to update the year
    public void setYear(int year) {
        this.year = year;
    }
}