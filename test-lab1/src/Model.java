// A class to represent the model of a motorcycle
class Model {
    // A private field to store the model name
    private String name;

    // A constructor to initialize the model name
    public Model(String name) {
        this.name = name;
    }

    // A getter method to return the model name
    public String getName() {
        return name;
    }

    // A setter method to update the model name
    public void setName(String name) {
        this.name = name;
    }
}