// A class to represent the brand of a motorcycle
class Brand {
    // A private field to store the brand name
    private String name;

    // A constructor to initialize the brand name
    public Brand(String name) {
        this.name = name;
    }

    // A getter method to return the brand name
    public String getName() {
        return name;
    }

    // A setter method to update the brand name
    public void setName(String name) {
        this.name = name;
    }
}